<h1>How you can help</h1>
<p>Having Fairtrade status is not the end of the journey! We continue to
	seek to promote Fairtrade in the shops, catering establishments,
	community organisations and workplaces of the borough.</p>
<p>
	If you are aware of any place that offers Fairtrade goods, either
	exclusively or alongside non-Fairtrade brands, we would be very glad to
	hear from you: please <a href="page.php?id=contacts">let us know</a>.
	This is not restricted to places where Fairtrade goods are sold; if,
	for example, your workplace uses Fairtrade tea and/or coffee in the
	staff room, or your school or community organisation uses or learns
	about Fairtrade in any way, we would love to know about it.
</p>
<p>If you know of a place which doesn't offer Fairtrade, and you would
	like them to do so, please encourage/ask/pester them! - and get in
	touch with us if you would like some help. We are very willing to come
	and give a talk, or to share ideas and resources.</p>
<p>
	If you are interested in becoming involved with the working group,
	please get in touch.
</p>
