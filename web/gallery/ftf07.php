<h1>Fairtrade Fortnight 2007</h1>
<p>We held a highly successful event for Fairtrade Fortnight 2007 in
	Fareham Shopping Centre, celebrating the recognition of Fareham as a
	Fairtrade Borough and spreading the word to even more people. We also
	had displays in Stubbington and Fareham libraries, and several churches
	in the town held Fairtrade sales to coincide with the Fortnight.</p>
