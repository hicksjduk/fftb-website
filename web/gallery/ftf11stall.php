<h1>Fairtrade Fortnight 2011</h1>
<p>We held a very successful stall in Fareham Shopping Centre on
	Saturday 5th March. We were pleased to welcome the Mayor and Mayoress
	of Fareham, Cllrs Brian and Susan Bayford, and other members of the
	Borough Council including the Leader, Cllr Se&aacute;n Woodward, and
	also Fareham's MP (and Financial Secretary to the Treasury) Mark Hoban.
	We appreciated the support of the Co-Op, Sainsbury's, Marks &amp;
	Spencer and Boots, all of whom gave us or lent us goods for our
	display.</p>
<p>With the emphasis of this year's Fortnight being on cotton, we joined
	in the national drive to decorate as much Fairtrade cotton bunting as
	possible, and many people of all ages added their voice and their
	creativity to the celebration of Fairtrade. But the star of the show
	was a stunning Fairtrade dress, designed and created from Fairtrade
	cotton bags by local costume design graduate Emily Sghendo.</p>
