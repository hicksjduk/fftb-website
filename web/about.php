<h1>About us</h1>
<p>The borough of Fareham is located in south Hampshire, between the
Fairtrade cities of Portsmouth and Southampton. It includes&nbsp;-
besides the town of Fareham itself&nbsp;- Portchester to the east,
Stubbington and Hill Head to the south, and Titchfield, Locks Heath,
Park Gate, Swanwick, Warsash, Sarisbury and Whiteley to the west. The
population of the borough is a little over 110,000. For more information about
the borough, visit the website of <a href="http://www.fareham.gov.uk/">Fareham
Borough Council</a>.</p>
<p>The Fairtrade Borough working group was formed in June 2005, with
the aim of promoting Fairtrade in Fareham and fulfilling the five
criteria for a Fairtrade Town. This aim was achieved in February 2007
with the recognition of Fareham as a Fairtrade Borough, and we have
successfully renewed our Fairtrade Borough status at two-yearly intervals
from 2008 to 2020. The group disbanded in 2022.</p>
