
<h1>Fairtrade Fortnight 2016</h1>
<p>We held a very successful stall in Fareham Shopping Centre on
	Saturday 27th February. We were pleased to welcome the Mayor and Mayoress of
	Fareham, Cllr Mike and Mrs Anne Ford, and our new MP Suella Fernandes.</p>
<p>The centrepiece of our stall was a chocolate fountain, courtesy of Fareham Community Church
and award-winning chocolatier David Greenwood-Haigh.</p>
