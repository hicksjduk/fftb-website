
<h1>Fairtrade Celebration Meal - 8th March 2012</h1>
<p>Catering students at Fareham College rose successfully to the
	challenge of Fairtrade as part of Fareham's celebrations of Fairtrade
	Fortnight. They had been set the task, by the Fairtrade campaign groups
	in Fareham and Gosport, of devising a three-course menu using as many
	Fairtrade ingredients as possible; and the best dishes were served to
	around 40 guests at a special dinner in the college's Chequers
	Restaurant.</p>
<p>After bucks fizz and canap&eacute;s, the guests - who included
	college principal Nigel Duncan and Fairtrade campaigners from both
	Gosport and Fareham - were treated to a pineapple and avocado salad
	with sticky balsamic glaze; middle-eastern style stuffed chicken with
	jewelled quinoa and a fresh tomato compote, and a trio of desserts
	including cr&egrave;me caramel with tea-soaked date pur&eacute;e, lime
	and ginger sorbet, and banoffee cheesecake. This was washed down with
	Fairtrade wine and orange juice, and followed by coffee and tea with
	mini chocolate brownies. The meal used over twenty different Fairtrade
	ingredients, and where elements were not available from Fairtrade
	sources locally-produced and organic alternatives were used wherever
	possible.</p>
<p>Nigel Duncan commended all the students who were involved in
	preparing and serving the meal, and expressed his pleasure at the
	collaboration between the college and local campaigners to boost the
	students' awareness of an important issue. He pledged the college to
	take further steps in support of Fairtrade: they will serve a Fairtrade
	dish every month in Chequers Restaurant, and will look at how they can
	increase their use of Fairtrade throughout the college.</p>
<p>Rachel Hicks, Chair of the Fareham Fairtrade Borough working group,
	said: "It's been great to work with the students, and we're delighted
	at how well they have engaged with Fairtrade. Buying Fairtrade goods -
	and local and organic too - is an easy way for us all to enjoy quality
	food in a way that puts the needs of people and the planet first.
	Everyone can play their part, and make a real difference for
	sustainability and for justice."</p>

