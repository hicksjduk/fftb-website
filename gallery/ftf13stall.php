
<h1>Fairtrade Fortnight 2013</h1>
<p>We held a very successful stall in Fareham Shopping Centre on
	Saturday 9th March. We were pleased to welcome the Mayor and Mayoress
	of Fareham, Cllr Dennis and Mrs Carole Steadman, and other members of
	the Borough Council including the Leader, Cllr Se&aacute;n Woodward,
	and also Fareham's MP Mark Hoban.</p>
<p>We were pleased to display artwork on the theme of Fairtrade, that
	had been produced by students from Neville Lovett School.</p>
<p>A selection of photographs from this event can be seen below; click
	on a small picture to see a larger version.</p>

