
<h1>Fairtrade Fortnight 2014</h1>
<p>We held a very successful stall in Fareham Shopping Centre on
	Saturday 22nd February. We were pleased to welcome the Mayor of
	Fareham, Cllr Susan Bayford, together with her Consort Cllr Brian
	Bayford, and other members of the Borough Council including the Leader,
	Cllr Se&aacute;n Woodward, and also Fareham's MP Mark Hoban.</p>
<p>This year's Fortnight focused on bananas, and as well as giving away
	bananas that had been kindly donated by the Co-Op store on Gudge Heath
	Lane, we had a life-size banana to promote the campaign!</p>
